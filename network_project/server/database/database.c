#include "database.h"
#include "filedriver.h"

static uint8_t request = 0;

/*-----------------------------------------------------------------------------------------------------------------------
                                                                                                               ROUTE BYTE
-------------------------------------------------------------------------------------------------------------------------
|    7    |    6    |    5    |    4    |    3    |     2    |     1     |     0     |  position
|   R/W   |    -    |    -    |    -    |    -    |   MMSGE  |   MSMPL   |   MCLNT   |  variable
-------------------------------------------------------------------------------------------------------------------------

------------------------------------------------------------------------------------------------------------------------*/

int main(void) {

  uint8_t result = 0;
  filed_t driver = {.route = 0x00};
  request |= (0 << 7) | (1 << MCLNT);

  result = file_driver(request, &driver);
  printf("test result: %d\n", result);
  free_driver(&driver);
  return 0;
}